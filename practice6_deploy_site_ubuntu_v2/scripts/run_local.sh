#!/usr/bin/env bash

# Локальный запуск проекта (удобно проверить перед деплоем).
# Скрипт создаёт виртуальное окружение рядом с приложением и запускает Flask.

set -euo pipefail

# Переходим в папку с приложением (чтобы relative пути точно работали)
cd "$(dirname "$0")/../new_year_application"

# Создаём venv, если его ещё нет. Если уже есть — команда просто перезапишет папку,
# поэтому обычно лучше сначала удалить .venv вручную, если что-то сломалось.
python3 -m venv .venv

# Активируем окружение (после этого python/pip будут из .venv)
. .venv/bin/activate

# Ставим зависимости
pip install -r ../requirements.txt

# Запускаем приложение
python3 app.py

"""
Задача 3. Дешифратор

Шифр 
- '.' после символа ничего не меняет, точку просто выкидываем
- '..' означает "удали предыдущий символ", обе точки тоже выкидываем

идём слева направо. Поэтому '...' работает так:
сначала '..' удаляет символ, потом оставшаяся '.' просто пропадает.
"""
from __future__ import annotations

import sys


def decrypt(cipher: str) -> str:
    # Собираем результат в списке, чтобы удалять последний символ было легко (pop()).
    result: list[str] = []
    i = 0
    n = len(cipher)

    while i < n:
        ch = cipher[i]

        # Обычный символ — просто добавляем в результат.
        if ch != ".":
            result.append(ch)
            i += 1
            continue

        # Если встретили точку, смотрим: это одиночная '.' или пара '..'.
        if i + 1 < n and cipher[i + 1] == ".":
            # '..' — удаляем предыдущий символ, если он есть.
            if result:
                result.pop()
            # Пропускаем обе точки.
            i += 2
        else:
            # Одиночная '.' — просто пропускаем её.
            i += 1

    return "".join(result)


if __name__ == "__main__":
    # Поддержка работы через pipe:
    # echo 'абраа..-.кадабра' | python3 cli/task3_decrypt.py
    data = sys.stdin.read()
    # echo обычно добавляет перевод строки, на всякий случай его убираем.
    data = data.rstrip("\n")
    print(decrypt(data))
